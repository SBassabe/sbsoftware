package it.sbsoft.beans;

public class BedObj {
	
    private String bed_num;
	private String bed_cod;
	private String room_num;
	private int x_val;
	private int y_val;
	
	public String getBed_num() {
		return bed_num;
	}
	public void setBed_num(String bed_num) {
		this.bed_num = bed_num;
	}
	public String getBed_cod() {
		return bed_cod;
	}
	public void setBed_cod(String bed_cod) {
		this.bed_cod = bed_cod;
	}
	public String getRoom_num() {
		return room_num;
	}
	public void setRoom_num(String room_num) {
		this.room_num = room_num;
	}
	public int getX_val() {
		return x_val;
	}
	public void setX_val(int x_val) {
		this.x_val = x_val;
	}
	public int getY_val() {
		return y_val;
	}
	public void setY_val(int y_val) {
		this.y_val = y_val;
	}
	    
}
