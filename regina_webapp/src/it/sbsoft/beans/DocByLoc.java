package it.sbsoft.beans;

public class DocByLoc {
	
	private String loc_id;
	private String doc_id;
	private String data_dal;
	private String data_al;
	private String num_stanza;
	private String cod_stanza;
	private String doc_name;
	
	public String getLoc_id() {
		return loc_id;
	}
	public void setLoc_id(String loc_id) {
		this.loc_id = loc_id;
	}
	public String getDoc_id() {
		return doc_id;
	}
	public void setDoc_id(String doc_id) {
		this.doc_id = doc_id;
	}
	public String getData_dal() {
		return data_dal;
	}
	public void setData_dal(String data_dal) {
		this.data_dal = data_dal;
	}
	public String getData_al() {
		return data_al;
	}
	public void setData_al(String data_al) {
		this.data_al = data_al;
	}
	public String getNum_stanza() {
		return num_stanza;
	}
	public void setNum_stanza(String num_stanza) {
		this.num_stanza = num_stanza;
	}
	public String getCod_stanza() {
		return cod_stanza;
	}
	public void setCod_stanza(String cod_stanza) {
		this.cod_stanza = cod_stanza;
	}
	public String getDoc_name() {
		return doc_name;
	}
	public void setDoc_name(String doc_name) {
		this.doc_name = doc_name;
	}
	
	
	
	
}
