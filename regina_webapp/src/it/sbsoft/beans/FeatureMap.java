package it.sbsoft.beans;

public class FeatureMap {
	
	private String building;
	private String room;
	private String xVal;
	private String yVal;
	private String featType;
	private String featDesc;
	
	public String getBuilding() {
		return building;
	}
	public void setBuilding(String building) {
		this.building = building;
	}
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	public String getxVal() {
		return xVal;
	}
	public void setxVal(String xVal) {
		this.xVal = xVal;
	}
	public String getyVal() {
		return yVal;
	}
	public void setyVal(String yVal) {
		this.yVal = yVal;
	}
	public String getFeatType() {
		return featType;
	}
	public void setFeatType(String featType) {
		this.featType = featType;
	}
	public String getFeatDesc() {
		return featDesc;
	}
	public void setFeatDesc(String featDesc) {
		this.featDesc = featDesc;
	}
	
	
	
		
	
}
