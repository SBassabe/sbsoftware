package it.sbsoft.beans;

public class OccByBed {
	
	private String bed_num;
	private String status;
	private String nome;
	private String sesso;
	private String data_dal;
	private String data_al;
	private String room;
	
	public String getBed_num() {
		return bed_num;
	}
	public void setBed_num(String bed_num) {
		this.bed_num = bed_num;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSesso() {
		return sesso;
	}
	public void setSesso(String sesso) {
		this.sesso = sesso;
	}
	public String getData_dal() {
		return data_dal;
	}
	public void setData_dal(String data_dal) {
		this.data_dal = data_dal;
	}
	public String getData_al() {
		return data_al;
	}
	public void setData_al(String data_al) {
		this.data_al = data_al;
	}
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	
	

}
